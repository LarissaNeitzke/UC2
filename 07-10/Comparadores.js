let numero1 = 90
let numero2 = 45


//COMPARAR se um é igual ao outro
console.log(numero1 === numero2) //FALSE

//COMPARAR se um é diferente do outro
console.log(numero1 !== numero2) //TRUE

//COMPARAR se um é maior que o outro
console.log(numero1 > numero2) //TRUE

//COMPARAR se um é MAIOR OU IGUAL que o outro
console.log(numero1 >= numero2) //TRUE

//COMPARAR se um é MENOR que o outro
console.log(numero1 < numero2) //FALSE

//COMPARAR se um é MENOR OU IGUAL ao outro
console.log(numero1 <= numero2) //FALSE

